        return abort(404);
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $about_show = About::find($id);
        return view('Admin.About.show_about', compact('about_show'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
         $about_update = About::find($id) ?? abort(403);
        return view('Admin.About.edit_about', compact('about_update'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'about_description'=>'required',
        ]);

        About::whereId($id)->update([
            'about_description'=>$request->input('about_description'),
        ]);

        return  redirect()->route('about.index')->withSuccess('Update edildi!!!');
    }

